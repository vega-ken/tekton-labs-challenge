const mongoose = require('mongoose');
const Schema = mongoose.Schema; // const { Schema } = mongoose;

const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    required: true
  }
});

mongoose.model('users', UserSchema); // users collection
